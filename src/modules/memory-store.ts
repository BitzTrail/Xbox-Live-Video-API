// **** TYPINGS **** //

type MemoryStory = {
	[type: string]: any;
};

// **** DEFINITIONS **** //

const store: MemoryStory = {};

// **** PUBLIC METHODS **** //

export const getFromStore = <T>(key: string) => {
	const k = store[key];
	return k !== void 0 ? (k as T) : void 0;
};

export const setToStore = (key: string, payload: any) => {
	store[key] = payload;
	return store;
};
